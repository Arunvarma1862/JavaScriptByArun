let num=[1,5,8,9,564];
console.log(num, typeof num);
  
// convert array to string
let a= num.toString();
console.log(a, typeof a);

// join
let m= num.join("_");
console.log(m, typeof m);

// pop
let r=num.pop();
console.log(num, r)

// push
let j= num.push(431);
console.log(num, j);

// shift
let k= num.shift();
console.log(k, num)

//unshift
let o= num.unshift(68);
console.log(o,num)